from __future__ import annotations

from datetime import datetime
from typing import List

from intelligence.benchmarks.benchmark_result import BenchmarkResult, aggregate_results


def generate_benchmark_report(
    results: List[BenchmarkResult],
    title: str = "Cross-Benchmark Evaluation Report",
) -> str:
    """Generate a unified Markdown report from multiple benchmark results.

    The report includes:
        - Per-dataset metrics table
        - Aggregate summary
        - Ranking table (by recall)
        - Statistical validation summary (when validation data is present)

    Args:
        results:  List of results from :class:`BenchmarkRunner`.
        title:    Report title.

    Returns:
        A Markdown-formatted report string.
    """
    lines: List[str] = [
        f"# {title}",
        "",
        f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        "",
        f"- **Datasets evaluated:** {len(results)}",
        "",
    ]

    if not results:
        lines.append("_No results to report._")
        lines.append("")
        return "\n".join(lines)

    # -- Aggregate summary --
    agg = aggregate_results(results)
    lines.extend(
        [
            "## Aggregate Summary",
            "",
            "| Metric | Value |",
            "| ------ | ----- |",
            f"| Dataset Count | {agg['dataset_count']} |",
            f"| Total Queries | {agg['total_queries']} |",
            f"| Avg Recall | {_fmt_opt(agg['average_recall'])} |",
            f"| Avg Precision | {_fmt_opt(agg['average_precision'])} |",
            f"| Avg Latency (ms) | {agg['average_latency_ms']:.1f} |",
            f"| Avg Success Rate | {agg['average_success_rate']:.2%} |",
            f"| Avg Fallback Rate | {agg['average_fallback_rate']:.2%} |",
            "",
        ]
    )

    # -- Per-dataset metrics --
    lines.extend(
        [
            "## Per-Dataset Metrics",
            "",
            "| Dataset | Queries | Recall | Precision | Latency (ms) | Success | Fallback |",
            "| ------- | ------- | ------ | --------- | ------------ | ------- | -------- |",
        ]
    )
    for r in results:
        lines.append(
            f"| {r.dataset_name:<20} "
            f"| {r.query_count:>7} "
            f"| {_fmt_opt(r.average_recall):<10} "
            f"| {_fmt_opt(r.average_precision):<10} "
            f"| {r.average_latency_ms:>8.1f} "
            f"| {r.success_rate:>5.1%} "
            f"| {r.fallback_rate:>5.1%} |"
        )
    lines.append("")

    # -- Ranking table --
    sorted_by_recall = sorted(
        [r for r in results if r.average_recall is not None],
        key=lambda x: x.average_recall or 0.0,
        reverse=True,
    )
    if sorted_by_recall:
        lines.extend(
            [
                "## Ranking by Recall",
                "",
                "| Rank | Dataset | Recall |",
                "| ---- | ------- | ------ |",
            ]
        )
        for rank, r in enumerate(sorted_by_recall, start=1):
            lines.append(
                f"| {rank:>4} | {r.dataset_name:<20} | {r.average_recall:.4f} |"
            )
        lines.append("")

    # -- Statistical validation --
    has_validation = any(r.validation is not None for r in results)
    if has_validation:
        lines.extend(
            [
                "## Statistical Validation",
                "",
                "The following significance tests compare each dataset's recall "
                "against a zero baseline.",
                "",
            ]
        )
        for r in results:
            if r.validation is None:
                continue
            v = r.validation
            lines.append(f"### {r.dataset_name}")
            lines.append("")
            lines.append(f"**Summary:** {v.summary}")
            lines.append("")
            if v.significance:
                lines.append("| Test | Statistic | p-value | Significant? |")
                lines.append("| ---- | --------- | ------- | ------------ |")
                for name, sig in v.significance.items():
                    lines.append(
                        f"| {name:<20} | {sig.statistic:>9.4f} | "
                        f"{sig.p_value:>7.4f} | "
                        f"{'Yes' if sig.significant else 'No':<4} |"
                    )
                lines.append("")

    lines.append("---")
    lines.append("*Report generated by cross-benchmark evaluation framework*")
    lines.append("")
    return "\n".join(lines)


def _fmt_opt(val: object, fmt: str = ".4f") -> str:
    if val is None:
        return "N/A"
    if isinstance(val, float):
        return f"{val:{fmt}}"
    return str(val)
